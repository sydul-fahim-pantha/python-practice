# elseif
a = 100
b = 10
print("a " + str(a) + "  b " + str(b))

if (a == b) : 
  print("a==b if executed")
  print("a==b if ended")
elif (a > b) :
  print("a>b else")
  print("a>b else ended")
elif (a < b) :
  print("a<b else")
  print("a<b else ended")
else :
  print("default else ")

print("End of code")
 


