#!/usr/bin/python3

# single if 
a = 100
if (a == 100) : print("a == 100 if executed ")


# if-else

b = 10
print("a " + str(a) + "  b " + str(b))

if (a == b) : 
  print("a==b if executed")
  print("a==b if ended")
else :
  print("a==b else")
  print("a==b else ended")


c = 10
print("a " + str(a) + "  c " + str(c))

if (a != c) : 
  print("a!=c if executed")
  print("a!=c if ended")
else :
  print("a<>c else")
  print("a<>c else ended")
 


